
public class User {
    private String name;
    private double totalPaid;
    public User(String name) {
        this.name = name;
        this.totalPaid = 0.0;
    }
    public String getName() {
        return name;
    }
    public double getTotalPaid() {
        return totalPaid;
    }
    public void addPayment(double amount) {
        totalPaid += amount;
    }
}

